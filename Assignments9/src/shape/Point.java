package shape;

public class Point 
{
    private int xCordinate,yCordinate;
    public Point(int x,int y)
    {
        this.xCordinate=x;
        this.yCordinate=y;
    }
    int getXCordinate()
    {
        return this.xCordinate;
    }
    int getYCordinate()
    {
        return this.yCordinate;
    }
}
